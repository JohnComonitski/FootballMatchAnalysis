def get_xg(coords, invert = False):
    x, y = coords

    if invert:
        x *= -1
        y *= -1
        coords = (x, y)

    if( x < -53 or x > 53 ):
        return 0
    
    if( y < -34 or y > 34 ):
        return 0

    boxes = get_four_boxes(coords)

    corners = []
    for box in boxes:
        corners.append( { "value" : box["value"],  "point" : get_center(box["box"])} )

    pts = [(float(d['value']), d['point'][0], d['point'][1]) for d in corners]

    xs = [p[1] for p in pts]
    ys = [p[2] for p in pts]
    x0, x1 = min(xs), max(xs)
    y0, y1 = min(ys), max(ys)

    mapping = {(px, py): val for val, px, py in pts}

    if x1 == x0 or y1 == y0:
        return sum(mapping.values()) / 4.0

    v00 = mapping[(x0, y0)]
    v10 = mapping[(x1, y0)]
    v01 = mapping[(x0, y1)]
    v11 = mapping[(x1, y1)]

    u = (x - x0) / (x1 - x0)
    v = (y - y0) / (y1 - y0)

    return round((1-u)*(1-v)*v00 + u*(1-v)*v10 + (1-u)*v*v01 + u*v*v11, 4)
