def boxes_missing(quandrant, zone):
    box1 = None
    box2 = None
    box3 = None
    box4 = None
    box5 = None
    box6 = None
    box7 = None
    box8 = None

    if quandrant == 1:
        if zone % 16 == 1:
            box1 = True
            box2 = True
        if zone < 17:
            box1 = True
            box4 = True
    elif quandrant == 2:
        if zone % 16 == 1:
            box2 = True
            box3 = True
        if zone > 180:
            box3 = True
            box5 = True
    elif quandrant == 3:
        if zone % 16 == 0:
            box6 = True
            box7 = True
        if zone < 17:
            box4 = True
            box6 = True
    elif quandrant == 4:
        if zone % 16 == 0:
            box7 = True
            box8 = True
        if zone > 384:
            box5 = True
            box8 = True

    return [ box1, box2, box3, box4, box5, box6, box7, box8 ]

def get_four_boxes(coords):
    mapping = xg_map_zones()
    zone = int(get_zone(coords))
    quandrant = get_quadrant(coords, str(zone), mapping)
    x, y = coords

    directions = {
        "1" : ( -4.24, 4.25 ),
        "2" : ( 0, 4.25 ),
        "3" : ( 4.24, 4.25 ),
        "4" : ( -4.24, 0 ),
        "5" : ( 4.24, 0 ),
        "6" : ( -4.24, -4.25 ),
        "7" : ( 0, -4.25 ),
        "8" : ( 4.24, -4.25 )
    }

    boxes_to_invent = boxes_missing(quandrant, zone)

    val = zone_to_xg(str(zone))
    boxes = [ { "value" : val, "box" : mapping[str(zone)] } ]

    needed = []
    if quandrant == 1:
        needed = [ 1, 2, 4 ]
    elif quandrant == 2:
        needed = [ 2, 3, 5 ]
    elif quandrant == 3:
        needed = [ 4, 6, 7 ]
    elif quandrant == 4:
        needed = [ 5, 7, 8 ]

    for box in needed:
        direction = directions[str(box)]
        new_box = {
            "value" : 0,
            "box" : None
        }

        if boxes_to_invent[box - 1]:
            new_box["value"] = val
            invented_box = []
            for p in mapping[str(zone)]:
                invented_box.append([ p[0] + direction[0], p[1] + direction[1] ])
            new_box["box"] = invented_box
        else:
            new_x = x + direction[0]
            new_y = y + direction[1]
            new_zone = get_zone((new_x, new_y))
            new_box["value"] = zone_to_xg(new_zone)
            new_box["box"] = mapping[new_zone]

        boxes.append(new_box)
                
    return boxes

